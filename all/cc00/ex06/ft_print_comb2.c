#include <unistd.h>

void	ft_print_comb2(void)
{
	int x;
	int y;
	char c;
	char d;

	x = -1;
	while (++x <= 98)
	{
		y = x;
		while ((++y <= 99))
		{
			c = (x / 10 + '0');
			write(1, &c, 1);
			c = (x % 10 + '0');
			write(1, &c, 1);
			write(1, " ", 1);
			d = (y / 10 + '0');
			write(1, &d, 1);
			d = (y % 10 + '0');
			write(1, &d, 1);
			if (x != 98)
				write(1, ", ", 2);
		}

	}

}
