#include <unistd.h>

void ft_print_numbers(void)
{
	char a;
	char b;

	a = '0';
	b = '9';
	while (a <= b)
	{	
		write(1, &a, 1);
		a++;
	}
}

int main()
{
	ft_print_numbers();
}

